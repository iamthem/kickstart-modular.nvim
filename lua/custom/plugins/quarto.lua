return {
  { -- requires plugins in lua/plugins/treesitter.lua and lua/plugins/lsp.lua
    -- for complete functionality (language features)
    'quarto-dev/quarto-nvim',
    dev = false,
    ft = { 'markdown', 'quarto' },
    opts = {
      lspFeatures = {
        enabled = true,
        chunks = 'curly',
        languages = { 'r', 'python' },
        diagnostics = {
          enabled = true,
          triggers = { 'BufWritePost' },
        },
        completion = {
          enabled = true,
